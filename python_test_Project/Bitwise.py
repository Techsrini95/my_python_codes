#  bitwise operators - to save negative numbers (~,

a = 12        # tilde convert binary format of 12 to 13
b = 15
c = ~a

print(c)          # bitwise
print(a & b)      # bitwise and
print(a | b)      # bitwise or
print(a ^ b)       # xor odd are 1
print(a << 2)      # left shifts binary digits to left for 2 digits ( 10110 to 1011000)
print(a >> 2)      # right shift
